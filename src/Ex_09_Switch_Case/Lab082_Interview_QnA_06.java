package Ex_09_Switch_Case;

public class Lab082_Interview_QnA_06 {

    public static void main(String[] args) {

        char code = 'A';
        switch (code) {
            default:
                System.out.println("Hellooooooo");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
        }
    }

}
