package Ex_03_Literals;

public class Lab024_Literals_01 {

    public static void main(String[] args) {

        int i = 10;
        System.out.println(i);
        // int --> Integer Literal

        final float f =3.14F;
        System.out.println(f);
        // float --> Floating literal

        System.out.println("Literals are the actual values assigned");
        System.out.println("Literals can be Numeric and Non Numeric");

        // Syntax for Literal
        // data_types variable_name (identifier) = variable_value ( literal)

        /*
        Types of Literals
        1. Boolean Literals - true, false
        2. Character Literals
        3. String Literals
        4. Integral Literals
        5. Floating Literals
        6. null Literal , **Scientific** Literals
         */
    }
}
