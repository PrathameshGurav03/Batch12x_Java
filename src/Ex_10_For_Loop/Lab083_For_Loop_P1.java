package Ex_10_For_Loop;

public class Lab083_For_Loop_P1 {

    public static void main(String[] args) {

        // For Loop
        // Help you to repeat a block of code.

        // Initialization -> Condition -> Updation (Increment /decrement)
        // ICU

        for (int i=0; i<10 ;i++)
        {
            System.out.println(i);
        }

    }
}
