package Ex_10_For_Loop;

public class Lab091_For_Loop_Decrement {

    public static void main(String[] args) {

        for (int i = -1; i > -10; i--) {
            System.out.println(i);
        }
        System.out.println();
        int i = 10;
        for (; i > 0; i--) {
            System.out.println(i);
        }
    }
}
