package Ex_04_Operators;

public class Lab_030_Arithmetic_Operator {

    public static void main(String[] args) {

        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder

        int a = 20, b=2;
        float c=3.14f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        System.out.println("a+b");// String always written in " "
        System.out.println(a/c);



    }
}
