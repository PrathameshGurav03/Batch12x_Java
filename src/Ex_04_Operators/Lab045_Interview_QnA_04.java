public class Lab045_Interview_QnA_04 {

    public static void main(String[] args) {

        char c1 = 'A'; // ASCII -> 65
        char c2 = 'B'; // 66
        System.out.println(c1 + c2);
        // a1+b1 - 65+66 -> 131

        System.out.println('A'==66); // ASCII -> 65

        System.out.println();
        short s1 = 10;
        char c3 =  'A';
        System.out.println(s1+c3);

    }
}
