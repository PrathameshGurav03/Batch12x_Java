package Ex_04_Operators;

public class Lab035_Interview_Concat_Plus {

    public static void main(String[] args) {

        // concatenation
        String s1 = "Pratham";
        String s2 = "Gurav";
        System.out.println(s1 + s2); // Joins the string called concatenation.

        int a = 10;
        int b = 20;
        System.out.println(a + b); // Math Operation

        // + -> behave differently with the data type.
        // + -> operator overloading
    }
}
