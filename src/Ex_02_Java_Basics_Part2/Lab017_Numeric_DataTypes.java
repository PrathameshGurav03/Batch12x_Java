package Ex_02_Java_Basics_Part2;

public class Lab017_Numeric_DataTypes {

    public static void main(String[] args) {

        //byte b = 128; // Range of byte container max and min value -128 to 127
        byte b = 10;
        int i = 10;
        short s = 10;
        char c  = 'A';
        long l = 9876543210l; //( lower letter or upper letter case 'L' is mandatory for long datatype )
        long l1 = 9876543210L;

        float f = 3.14f;
        float f1 = 3.14F; //( lower letter or upper letter case 'F' is mandatory for float datatype )
        System.out.println(f1);

        double d = 3.123454345343;
        System.out.println(d);
    }
}
