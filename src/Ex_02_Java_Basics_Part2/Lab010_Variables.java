package Ex_02_Java_Basics_Part2;

public class Lab010_Variables {
    public static void main(String[] args) {

        System.out.println(" Store the value - which can be changed in the program");
        System.out.println(" Each variable should be given a unique name (identifier)");
        System.out.println(" Memory will be allocated for the variable while executing the program");
        System.out.println(" Value of the variable can be changed any number of times during the program execution");

        byte age= 25;
        System.out.println(age);

        // byte - data type -  type of container
        // age - Variable name
        // = - Assignment operator
        // 25 - Variable Value ( Literal )

        // --> Syntax for Variable
        /*
        data_type variable_name = variable_value ( Also called as literal )

        variable_name -> identifier
        variable_value -> literal
        data_type ->  int, short, byte, long, float, boolean,...
         */
    }
}
